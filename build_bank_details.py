html = Path('index.html').read_text(encoding='utf-8')
pattern = re.compile(r'<h4 class="bank-name"(?: itemprop="name")?>([^<]+)</h4>[\s\S]*?<a href="tel:([^"\s]+)" class="contact-phone"[\s\S]*?<div class="bank-actions">\s*<a href="bank-details.html\?bank=([^"\s]+)" class="bank-detail-link">[\s\S]*?<a href="([^"\s]+)" target="_blank"[^>]* class="bank-link">')
link_data = {m.group(3): {'name': m.group(1), 'phone': m.group(2), 'site': m.group(4)} for m in pattern.finditer(html)}
