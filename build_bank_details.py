output_lines = ["const bankDetailsData = {"]
for slug, meta in bank_meta.items():
    info = link_data.get(slug)
    if not info:
        raise ValueError(f'Missing link data for {slug}')
    meta_dict = {
        'slug': slug,
        'name': info['name'],
        'fullName': meta['full_name'],
        'summary': meta['summary'],
        'founded': meta['founded'],
        'headquarters': meta['headquarters'],
        'phone': info['phone'],
        'website': info['site'],
        'history': meta['history'],
        'currentStatus': meta['current_status'],
        'services': meta['services']
    }
    formatted_lines = json.dumps(meta_dict, ensure_ascii=False, indent=4).splitlines()
    entry_lines = [f"    '{slug}': {formatted_lines[0]}"]
    entry_lines.extend(f"    {line}" for line in formatted_lines[1:])
    entry_lines[-1] = entry_lines[-1] + ','
    output_lines.extend(entry_lines)
output_lines.append('};\n\nexport default bankDetailsData;\n')
Path('bank-details.js').write_text('\n'.join(output_lines), encoding='utf-8')
